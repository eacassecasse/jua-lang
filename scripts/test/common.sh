#!/usr/bin/env bash

set -euxo pipefail

###############################################################################
# Logging
###############################################################################

header() {

    printf "\n============================================================\n"
    printf "%s\n" "$1"
    printf "============================================================\n"

}

info() {

    printf "  • %s\n" "$1"

}


success() {

    printf "SUCCESS: %s\n" "$1"
}

error() {

    printf "ERROR: %s\n" "$1"
}

###############################################################################
# File utilities
###############################################################################

ensure_directory() {

    mkdir -p "$1"

}

write_file() {

    local destination="$1"

    shift

    ensure_directory "$(dirname "$destination")"

    cat > "$destination"

}
